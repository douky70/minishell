#include <string.h>
#include <stdlib.h>
#include "libft.h"

void	*ft_memdup(const void *src, size_t n)
{
	size_t	i;
	void	*dst;

	if (!(dst = malloc(n)))
		return (NULL);
	i = 0;
	while (i < n)
	{
		((unsigned char*)dst)[i] = ((unsigned char*)src)[i];
		i++;
	}
	return (dst);
}
